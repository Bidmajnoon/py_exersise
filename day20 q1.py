import requests
url = 'http://www.gutenberg.org/files/1112/1112.txt'
responce = requests.get(url)

word = responce.text
frequent = {}
words = word.split()
for i in words:

    if i in frequent:
        frequent[i] += 1
    else:
        frequent[i] = 1
sortwd_frequent = sorted(frequent.items() ,key=lambda x:x[1],reverse=True)

print(sortwd_frequent[:10])
    











































































































































































































































